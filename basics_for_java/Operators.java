public class Operators {
    public static void main(String[] args) {
        String str = "hello";
        Integer n = 100;
        Object n1 = new Integer(10);

        System.out.println(str instanceof String);
        System.out.println(n instanceof Integer);

        System.out.println(n1 instanceof String);
        System.out.println(n1 instanceof String);
    }
}
